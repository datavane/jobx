package com.jobxhub.common.util;

import java.util.*;

public class ConsistentHash<T> {

    private final MurmurHash murmurHash;
    private int numberOfReplicas = 2 << 3;
    private final SortedMap<Long, T> circle = new TreeMap<Long, T>();

    public ConsistentHash(Collection<T> nodes) {
        this.murmurHash = new MurmurHash();
        for (T node : nodes) {
            add(node);
        }
    }

    public void add(T node) {
        for (int i = 0; i < numberOfReplicas; i++) {
            circle.put(murmurHash.hash64(node.toString() + i), node);
        }
    }

    public void remove(T node) {
        for (int i = 0; i < numberOfReplicas; i++) {
            circle.remove(murmurHash.hash64(node.toString() + i));
        }
    }

    public T get(Object key) {
        if (circle.isEmpty()) {
            return null;
        }
        long hash = murmurHash.hash64(key.toString());
        if (!circle.containsKey(hash)) {
            SortedMap<Long, T> tailMap = circle.tailMap(hash);
            hash = tailMap.isEmpty() ? circle.firstKey() : tailMap.firstKey();
        }
        return circle.get(hash);
    }

    public long getSize() {
        return circle.size();
    }


}

